const FEET_FRAMES = [DINO_FEET_BASE, DINO_FEET_LEFT, DINO_FEET_RIGHT];

function renderShadow(frame) {
  const pixels = [...DINO_BODY, ...frame];
  return pixels
    .map((pixel, index) => {
      const x = 24 * (index % 24);
      const y = 24 * Math.floor(index / 24);
      return `${x}px ${y}px ${DINO_COLORS[pixel]}`;
    })
    .join(', ');
}

const DINO_SHADOW_FRAMES = FEET_FRAMES.map(renderShadow);

export default function LoginPixelDino() {
  const [frameIndex, setFrameIndex] = useState(0);

  useEffect(() => {
    const timer = window.setInterval(() => {
      setFrameIndex((prev) => (prev + 1) % DINO_SHADOW_FRAMES.length);
    }, 100);

    return () => window.clearInterval(timer);
  }, []);

  return (
    <aside
      className="login-dino-loader"
      style={{ '--wh-number': 24, '--shadow': DINO_SHADOW_FRAMES[frameIndex] }}
      aria-hidden="true"
    >
      <div className="login-dino-loader__pixel" />
    </aside>
  );
}
